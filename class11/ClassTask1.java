package com.class11;

public class ClassTask1 {

	public static void main(String[] args) {
 
		int [][]num= {
				{3,5,6,7},
				{5,6,7,8},
				{6,7,8,9}
		};
		
		for (int i=0;i<num.length;i++) {
			for (int j=0;j<num[i].length;j++) {
				System.out.print(num[i][j]+" ");
			}
			System.out.println();
		}
		
		
		
		
	}

}
